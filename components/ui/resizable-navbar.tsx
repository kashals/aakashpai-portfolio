"use client";
import { cn } from "@/lib/utils";
import { Menu, X } from "lucide-react";
import {
  motion,
  AnimatePresence,
  useScroll,
  useMotionValueEvent,
} from "motion/react";

import React, { useRef, useState } from "react";


interface NavbarProps {
  children: React.ReactNode;
  className?: string;
}

interface NavBodyProps {
  children: React.ReactNode;
  className?: string;
  visible?: boolean;
}

interface NavItemsProps {
  items: {
    name: string;
    link: string;
  }[];
  className?: string;
  onItemClick?: () => void;
  activeSection?: string;
}

interface MobileNavProps {
  children: React.ReactNode;
  className?: string;
  visible?: boolean;
}

interface MobileNavHeaderProps {
  children: React.ReactNode;
  className?: string;
}

interface MobileNavMenuProps {
  children: React.ReactNode;
  className?: string;
  isOpen: boolean;
  onClose: () => void;
}

export const Navbar = ({ children, className }: NavbarProps) => {
  const ref = useRef<HTMLDivElement>(null);
  const { scrollY } = useScroll({
    target: ref,
    offset: ["start start", "end start"],
  });
  const [visible, setVisible] = useState<boolean>(false);

  useMotionValueEvent(scrollY, "change", (latest) => {
    if (latest > 100) {
      setVisible(true);
    } else {
      setVisible(false);
    }
  });

  return (
    <motion.div
      ref={ref}
      className={cn("fixed inset-x-0 top-6 z-50 w-full flex justify-center", className)}
    >
      {React.Children.map(children, (child) =>
        React.isValidElement(child)
          ? React.cloneElement(
              child as React.ReactElement<{ visible?: boolean }>,
              { visible },
            )
          : child,
      )}
    </motion.div>
  );
};

export const NavBody = ({ children, className, visible }: NavBodyProps) => {
  return (
    <motion.div
      animate={
        {
          backdropFilter: visible ? "blur(10px)" : "none",
          WebkitBackdropFilter: visible ? "blur(10px)" : "none",
          boxShadow: visible
            ? "0 0 24px rgba(34, 42, 53, 0.06), 0 1px 1px rgba(0, 0, 0, 0.05), 0 0 0 1px rgba(34, 42, 53, 0.04), 0 0 4px rgba(34, 42, 53, 0.08), 0 16px 68px rgba(47, 48, 55, 0.05), 0 1px 0 rgba(255, 255, 255, 0.1) inset"
            : "none",
          width: visible ? "min(800px, 90vw)" : "100%",
          y: visible ? 20 : 0,
        } as any
      }
      transition={{
        type: "spring",
        stiffness: 200,
        damping: 50,
      }}
      style={{}}
      className={cn(
        "relative z-[60] mx-auto hidden w-full max-w-7xl flex-row items-center justify-between self-start px-6 h-16 lg:flex",
        visible ? "bg-[var(--surface)] border border-[var(--border)] rounded-md" : "bg-transparent",
        className,
      )}
    >
      {children}
    </motion.div>
  );
};

export const NavItems = ({ items, className, onItemClick, activeSection }: NavItemsProps) => {
  return (
    <motion.div
      className={cn(
        "hidden flex-1 flex-row items-center justify-center gap-8 lg:flex mx-8",
        className,
      )}
    >
      {items.map((item, idx) => {
        const isActive = activeSection === item.link.replace("#", "");
        return (
          <a
            onClick={onItemClick}
            className={cn("text-xs tracking-[0.15em] uppercase transition-colors duration-150", isActive ? "text-text" : "text-muted")}
            style={{ color: isActive ? "var(--text)" : "var(--muted)" }}
            onMouseEnter={(e) => (!isActive && (e.currentTarget.style.color = "var(--text)"))}
            onMouseLeave={(e) => (!isActive && (e.currentTarget.style.color = "var(--muted)"))}
            key={`link-${idx}`}
            href={item.link}
          >
            {item.name}
          </a>
        );
      })}
    </motion.div>
  );
};

